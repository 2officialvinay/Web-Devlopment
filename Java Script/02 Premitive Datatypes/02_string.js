let str1 = "Mobile";
console.log(typeof(str1));

let str2 = "Charger";
console.log(typeof(str2));

// Concatenation of strings using '+' :
let str3 = str1 + " and " + str2 + " are made for each other.";
console.log(str3);

// Concatenation using string interpolation using template literals(``):
let str4 = `${str1} and ${str2} are made for each other.`;
console.log(str4);